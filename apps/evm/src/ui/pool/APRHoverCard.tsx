'use client'

import { Pool } from '@sushiswap/client'
import {
  CardDescription,
  Currency,
  HoverCard,
  HoverCardContent,
  HoverCardTrigger,
  Popover,
  PopoverContent,
  PopoverTrigger,
  Separator,
} from '@sushiswap/ui'
import { FC, ReactNode } from 'react'
import { ChainId } from 'sushi/chain'
import { tryParseAmount } from 'sushi/currency'
import { formatPercent } from 'sushi/format'

import { incentiveRewardToToken } from '../../lib/functions'

interface APRHoverCardProps {
  children: ReactNode
  pool: Pool
  showEmissions?: boolean
  smartPoolAPR?: number
}

export const APRHoverCard: FC<APRHoverCardProps> = ({
  children,
  pool,
  showEmissions = true,
  smartPoolAPR,
}) => {
  const totalAPR =
    (typeof smartPoolAPR === 'number'
      ? smartPoolAPR * 100
      : pool.feeApr1d * 100) +
    pool.incentives
      .filter((el) => +el.rewardPerDay > 0)
      .reduce((acc, cur) => acc + cur.apr * 100, 0)

  const card = (
    <>
      <div className="p-6">
        <CardDescription className="text-xs mb-6">
          APR is calculated based on the fees
          {pool.isIncentivized ? ' and rewards' : ''} generated by the pool over
          the last 24 hours. The APR displayed is algorithmic and subject to
          change.
        </CardDescription>
        <div className="flex flex-col gap-3">
          {typeof smartPoolAPR === 'number' ? (
            <div className="flex justify-between gap-1 items-center">
              <span className="flex flex-grow text-sm text-muted-foreground">
                Smart pool APR
              </span>
              <span className="text-right text-sm">
                {formatPercent(smartPoolAPR)}
              </span>
            </div>
          ) : (
            <div className="flex justify-between gap-1 items-center">
              <span className="flex flex-grow text-sm text-muted-foreground">
                Fees
              </span>
              <span className="text-right text-sm">
                {formatPercent(pool.feeApr1d)}
              </span>
            </div>
          )}
          <div className="flex flex-col gap-1">
            <div className="flex justify-between gap-1 items-center">
              <span className="flex flex-grow text-sm text-muted-foreground">
                Rewards
              </span>
              <span className="text-right text-sm">
                {formatPercent(pool.incentiveApr)}
              </span>
            </div>
            <div className="flex flex-col items-end">
              {pool.isIncentivized && showEmissions
                ? pool.incentives.map((el, i) => {
                    const amount = tryParseAmount(
                      el.rewardPerDay.toString(),
                      incentiveRewardToToken(el.chainId as ChainId, el),
                    )
                    if (!amount) return null

                    return (
                      <span
                        key={i}
                        className="flex gap-1 items-center text-[10px]"
                      >
                        +{' '}
                        <Currency.Icon
                          currency={amount?.currency}
                          width={10}
                          height={10}
                        />
                        {amount?.toSignificant(6)} {amount?.currency.symbol} per
                        day
                      </span>
                    )
                  })
                : null}
            </div>
          </div>
          <div className="flex items-center gap-2">
            <Separator className="text-muted-foreground" />
          </div>
          <div className="flex justify-between gap-1 items-center">
            <span className="flex flex-grow font-bold text-sm">Total APR</span>
            <span className="text-right text-sm font-bold">
              {formatPercent(totalAPR / 100)}
            </span>
          </div>
        </div>
      </div>
    </>
  )

  return (
    <>
      <div className="hidden sm:block">
        <HoverCard openDelay={300} closeDelay={0}>
          <HoverCardTrigger asChild>{children}</HoverCardTrigger>
          <HoverCardContent side="right" className="!p-0 max-w-[320px]">
            {card}
          </HoverCardContent>
        </HoverCard>
      </div>
      <div className="block sm:hidden">
        <Popover>
          <PopoverTrigger onClick={(e) => e.stopPropagation()} asChild>
            {children}
          </PopoverTrigger>
          <PopoverContent side="right" className="!p-0 max-w-[320px]">
            {card}
          </PopoverContent>
        </Popover>
      </div>
    </>
  )
}
